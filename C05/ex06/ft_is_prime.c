int	ft_is_prime(int nb)
{
	int	r;

	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	r = 2;
	while ((nb / r) >= r)
	{
		if (nb % r == 0)
			return (0);
		r++;
	}
	return (1);
}
